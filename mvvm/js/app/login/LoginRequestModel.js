/**
 * Login Request
 * @param {string} username
 * @param {string} password
 * @class {LoginRequestModel} LoginRequestModel
 */
function LoginRequestModel(username, password) {

    /**
     * username
     * @type {string}
     */
    this.username = username;

    /**
     * password
     * @type {string}
     */
    this.password = password;

}
